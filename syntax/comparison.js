//비교연산자에 대해서 알아 봅시다.
//1+1 가운데 '+' 연산자라고 한다. 왼쪽의 항과 오른쪽의 항을 계산해서 하나의 값으로 표현한다.
console.log(1+1);//숫자 2를 표현하는 방법: '2' or '1+1' 
console.log(1===1); // == 좌, 우가 같으면 true, 다르면 false. === 이꼴은 3개를 써라. 정확하게 같으냐?
console.log(1===2); //false
console.log(1>2); //false
console.log(1<2); //false
name = 1; // = 은 대입 연산자 
name == 1; //== 는 비교 연산자, 두개 ==를 쓸 때는 === 3개를 써라. 